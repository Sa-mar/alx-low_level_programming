/**
 * reverse_array - The function
 * @a: The array
 * @n: number of elemets
 *
 * Description: reverses contents of array
 *
 * Return: void
 */
void reverse_array(int *a, int n)
{
	int i, *rev;

	for (i = 0; i < n; i++)
	{
		rev[i] = a[n-i];
	}
}
